  function normalize(text) {
    return String(text || '').replace(/\s+/g, ' ').trim()
  }

  function translatePattern(text, language) {
    const patterns = patternTranslations[language] || []
    const normalized = normalize(text)

    for (const [pattern, replacement] of patterns) {
      if (pattern.test(normalized)) return normalized.replace(pattern, replacement)
    }

    return text
  }

  function translateText(text, dictionary, language = activeLanguage) {
    const normalized = normalize(text)
    if (!normalized) return text
    return dictionary[normalized] || translatePattern(text, language)
  }

  function translateNodeText(node, dictionary, language) {
    if (!node.__appTranslationOriginal) node.__appTranslationOriginal = node.nodeValue

    const original = node.__appTranslationOriginal
    if (language === 'fr') {
      node.nodeValue = original
      return
    }

    const translated = translateText(original, dictionary)
    if (translated === node.nodeValue) return

    const leading = original.match(/^\s*/)?.[0] || ''
    const trailing = original.match(/\s*$/)?.[0] || ''
    node.nodeValue = `${leading}${translated}${trailing}`
  }

  function translateAttribute(element, attribute, dictionary, language) {
    const originalAttribute = `data-i18n-original-${attribute}`
    const original = element.getAttribute(originalAttribute) || element.getAttribute(attribute)

    if (!original) return
    if (!element.hasAttribute(originalAttribute)) element.setAttribute(originalAttribute, original)
    element.setAttribute(attribute, language === 'fr' ? original : translateText(original, dictionary))
  }

  function translateElementText(element, dictionary, language) {
    const original = element.getAttribute('data-i18n-original-text') || element.textContent

    if (!original) return
    if (!element.hasAttribute('data-i18n-original-text')) {
      element.setAttribute('data-i18n-original-text', original)
    }

    element.textContent = language === 'fr' ? original : translateText(original, dictionary)
  }

  function shouldSkipElement(element) {
    if (!element) return true
    const tag = element.tagName
    return (
      element.closest('[data-no-translate]') ||
      ['SCRIPT', 'STYLE', 'CODE', 'PRE', 'TEXTAREA', 'INPUT'].includes(tag)
    )
  }

  function translatePage(language) {
    if (!supportedLanguages.includes(language)) language = 'fr'
    const dictionary = dictionaries[language] || {}

    activeLanguage = language
    document.documentElement.lang = language
    if (!document.__appTranslationOriginalTitle) {
      document.__appTranslationOriginalTitle = document.title
    }
    document.title =
      language === 'fr'
        ? document.__appTranslationOriginalTitle
        : translateText(document.__appTranslationOriginalTitle, dictionary)

    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        return shouldSkipElement(node.parentElement)
          ? NodeFilter.FILTER_REJECT
          : NodeFilter.FILTER_ACCEPT
      },
    })

    const nodes = []
    while (walker.nextNode()) nodes.push(walker.currentNode)
    nodes.forEach((node) => translateNodeText(node, dictionary, language))

    document
      .querySelectorAll(
        '[data-i18n], option, button, input[placeholder], textarea[placeholder], input[type="submit"], input[type="button"], [title], [aria-label]'
      )
      .forEach((element) => {
        if (shouldSkipElement(element) && !['INPUT', 'TEXTAREA'].includes(element.tagName)) return
        if (element.hasAttribute('data-i18n')) translateElementText(element, dictionary, language)
        if (element.placeholder) translateAttribute(element, 'placeholder', dictionary, language)
        if (
          element.tagName === 'INPUT' &&
          ['submit', 'button'].includes(String(element.type || '').toLowerCase()) &&
          element.value
        ) {
          translateAttribute(element, 'value', dictionary, language)
        }
        if (element.getAttribute('title')) {
          translateAttribute(element, 'title', dictionary, language)
        }
        if (element.getAttribute('aria-label')) {
          translateAttribute(element, 'aria-label', dictionary, language)
        }
        if (element.tagName === 'OPTION') translateElementText(element, dictionary, language)
      })
  }

  async function resolveLanguage() {
    const localLanguage = localStorage.getItem('appLanguage')
    const explicitLocalLanguage = localStorage.getItem('appLanguageExplicit') === 'true'
    if (explicitLocalLanguage && supportedLanguages.includes(localLanguage)) return localLanguage

    try {
      const response = await fetch('/api/settings/language', {
        headers: { Accept: 'application/json' },
      })
      if (!response.ok) throw new Error('Language endpoint unavailable')
      const data = await response.json()
      if (supportedLanguages.includes(data.language)) {
        return data.language
      }
    } catch {}

    const serverLanguage = window.__APP_LANGUAGE__
    if (supportedLanguages.includes(serverLanguage)) return serverLanguage

    return 'fr'
  }

  window.AppTranslations = {
    supportedLanguages,
    translatePage,
    translate(text, language = activeLanguage) {
      if (!supportedLanguages.includes(language)) language = 'fr'
      if (language === 'fr') return text
      return translateText(text, dictionaries[language] || {}, language)
    },
    setLanguage(language) {
      if (!supportedLanguages.includes(language)) return
      localStorage.setItem('appLanguage', language)
      localStorage.setItem('appLanguageExplicit', 'true')
      translatePage(language)
    },
  }

  const nativeConfirm = window.confirm.bind(window)
  const nativeAlert = window.alert.bind(window)

  window.confirm = (message) => nativeConfirm(window.AppTranslations.translate(message))
  window.alert = (message) => nativeAlert(window.AppTranslations.translate(message))

  document.addEventListener('DOMContentLoaded', async () => {
    const language = await resolveLanguage()
    translatePage(language)

    let pending = false
    const observer = new MutationObserver(() => {
      if (pending) return
      pending = true
      window.requestAnimationFrame(() => {
        pending = false
        const localLanguage = localStorage.getItem('appLanguage')
        const explicitLocalLanguage = localStorage.getItem('appLanguageExplicit') === 'true'
        const currentLanguage =
          explicitLocalLanguage && supportedLanguages.includes(localLanguage)
            ? localLanguage
            : activeLanguage || window.__APP_LANGUAGE__ || language
        translatePage(supportedLanguages.includes(currentLanguage) ? currentLanguage : language)
      })
    })

    observer.observe(document.body, {
      childList: true,
      subtree: true,
      characterData: true,
    })
  })
